    /**
     * Event handler untuk tombol Tambah.
     * Memvalidasi input dan menyimpan data baru ke database.
     */
    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnTambahActionPerformed
        try {
            validateInput();
            String nama = txtNama.getText().trim();
            String kategori = cbKategori.getSelectedItem().toString();
            int jumlah = (Integer) jSpinner1.getValue();
            String kondisi = getSelectedKondisi();
            String lokasi = txtLokasi.getText().trim();

            barangDAO.add(new Barang(0, nama, kategori, jumlah, kondisi, lokasi));
            clearFields();
            loadData();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }// GEN-LAST:event_btnTambahActionPerformed

    /**
     * Event handler untuk tombol Ubah.
     * Memvalidasi input dan mengupdate data yang dipilih di database.
     */
    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnUbahActionPerformed
        if (selectedId == null) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah");
            return;
        }

        try {
            Barang barang = new Barang(
                    selectedId,
                    txtNama.getText(),
                    cbKategori.getSelectedItem().toString(),
                    (Integer) jSpinner1.getValue(),
                    getSelectedKondisi(),
                    txtLokasi.getText()
            );

            barangDAO.update(barang);
            clearFields();
            loadData();
            selectedId = null;
            JOptionPane.showMessageDialog(this, "Data berhasil diubah");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error updating data: " + e.getMessage());
        }
    }// GEN-LAST:event_btnUbahActionPerformed

    /**
     * Event handler untuk tombol Hapus.
     * Menampilkan konfirmasi dan menghapus data yang dipilih dari database.
     */
    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnHapusActionPerformed
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus");
            return;
        }

        int id = (Integer) jTable1.getValueAt(row, 0);
        String nama = (String) jTable1.getValueAt(row, 1);

        showConfirmDialog(
                "Apakah Anda yakin ingin menghapus data " + nama + "?",
                () -> {
                    try {
                        barangDAO.delete(id);
                        loadData();
                        clearFields();
                        JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, "Error deleting data: " + e.getMessage());
                    }
                }
        );
    }// GEN-LAST:event_btnHapusActionPerformed

