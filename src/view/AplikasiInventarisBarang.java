    /**
     * Memuat ulang data dari database ke tabel.
     * Mengosongkan model tabel terlebih dahulu sebelum mengisi dengan data baru.
     */
    private void loadData() {
        tableModel.setRowCount(0);
        String sql = "SELECT * FROM barang";
        try (Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(sql)) {
            while (rs.next()) {
                tableModel.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nama"),
                    rs.getString("kategori"),
                    rs.getInt("jumlah"),
                    rs.getString("kondisi"),
                    rs.getString("lokasi")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }
    }

    /**
     * Melakukan validasi input sebelum menyimpan/mengubah data.
     * Memastikan field-field wajib telah diisi dengan benar.
     * 
     * @throws Exception jika ada input yang tidak valid
     */
    private void validateInput() throws Exception {
        if (txtNama.getText().trim().isEmpty()) {
            throw new Exception("Nama barang harus diisi");
        }
        if ((Integer) jSpinner1.getValue() < 0) {
            throw new Exception("Jumlah tidak boleh negatif");
        }
        if (!radioBaru.isSelected() && !radioBekas.isSelected() && !radioRusak.isSelected()) {
            throw new Exception("Pilih kondisi barang");
        }
        if (txtLokasi.getText().trim().isEmpty()) {
            throw new Exception("Lokasi penyimpanan harus diisi");
        }
    }

